package cn.tedu.entity;
/**
 * 公司员工奖惩信息类institution
 */

import java.io.Serializable;
import java.util.Date;


public class Institution implements Serializable {
	private int id;  //奖惩编号
	private String name;  //奖惩名称
	private String reason; //奖惩原因
	private String explain; //奖惩说明
	private Date createtime; //创建时间
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public String getExplain() {
		return explain;
	}
	public void setExplain(String explain) {
		this.explain = explain;
	}
	public Date getCreatetime() {
		return createtime;
	}
	public void setCreatetime(Date createtime) {
		this.createtime = createtime;
	}
	public Institution(int id, String name, String reason, String explain, Date createtime) {
		super();
		this.id = id;
		this.name = name;
		this.reason = reason;
		this.explain = explain;
		this.createtime = createtime;
	}
	@Override
	public String toString() {
		return "Institution [id=" + id + ", name=" + name + ", reason=" + reason + ", explain=" + explain
				+ ", createtime=" + createtime + "]";
	}
	
	
	
	
	
}
